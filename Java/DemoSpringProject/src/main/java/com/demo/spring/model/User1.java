package com.demo.spring.model;

public class User1 {
	public String name;
	public Integer age;
	public String gender;
	public User1(String name, Integer age, String gender) {
		this.name = name;
		this.age = age;
		this.gender = gender;
	}
	public String getName() {
		return name;
	}
	public Integer getAge() {
		return age;
	}
	public String getGender() {
		return gender;
	}
	
	
	
}
